"""
时间线 + 树形图示例
"""
import sciplot as sp

# 时间线
sp.timeline(
    events=[
        ("2024年3月",  "项目启动",       "确定研究方向与团队组建"),
        ("2024年5月",  "数据收集完成",   "收集标注 10 万条训练数据"),
        ("2024年8月",  "基线模型完成",   "实现三个对比基线方法"),
        ("2024年10月", "核心模型完成",   "提出改进方案并完成实验"),
        ("2024年12月", "论文初稿完成",   "完成实验验证与论文撰写"),
        ("2025年1月",  "最终提交",       "论文送审与答辩准备"),
    ],
    title="研究进度时间线",
)
sp.savefig("timeline_research")

# 树形图
sp.tree(
    nodes={
        "根": ["数据处理", "模型训练", "结果分析"],
        "数据处理": ["数据清洗", "特征工程", "数据增强"],
        "模型训练": ["基线模型", "改进模型", "消融实验"],
        "结果分析": ["定量分析", "可视化", "案例分析"],
        "数据清洗": ["去重", "去噪"],
        "特征工程": ["TF-IDF", "词嵌入"],
    },
    root="根",
    title="实验方案树形结构",
)
sp.savefig("tree_experiment")
